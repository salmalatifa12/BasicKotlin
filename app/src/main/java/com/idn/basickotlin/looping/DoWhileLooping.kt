package com.idn.basickotlin.looping

fun main() {
    // var angka = 3
    var angka = 6
    do {
        println("asyeek")
        angka++ // bernilai satu perulangan
    } while (angka < 5)

//ketika bernilai true akan menampilkan asyeek sejumlah 2 karena jarak antara 3-5 itu hanya ada nomor 4, jadi hanya 1 bilangan, menampilkan 2 kali karena ada 2 kali karena ada fungsi ++
//ketika bernilai true akan menampilkan  sejumlah perulangan yg dilakukan yaitu jumlah pengecekan dari do dan syarat dari while
}